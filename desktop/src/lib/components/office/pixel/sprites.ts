/** Generate a checkered floor tile pattern (16x16) */
export function checkerFloor(color1: string, color2: string): string[][] {
  const tile: string[][] = [];
  for (let y = 0; y < 16; y++) {
    const row: string[] = [];
    for (let x = 0; x < 16; x++) {
      const isAlt = (Math.floor(x / 8) + Math.floor(y / 8)) % 2 === 0;
      row.push(isAlt ? color1 : color2);
    }
    tile.push(row);
  }
  return tile;
}

/** Render any sprite to a cached canvas */
const spriteCanvasCache = new Map<string, HTMLCanvasElement>();

export function renderSpriteToCanvas(
  sprite: string[][],
  zoom: number,
  cacheKey?: string,
): HTMLCanvasElement {
  const key = cacheKey ? `${cacheKey}_${zoom}` : undefined;
  if (key && spriteCanvasCache.has(key)) return spriteCanvasCache.get(key)!;

  const h = sprite.length;
  const w = sprite[0]?.length ?? 0;
  const canvas = document.createElement("canvas");
  canvas.width = w * zoom;
  canvas.height = h * zoom;
  const ctx = canvas.getContext("2d")!;

  for (let r = 0; r < h; r++) {
    for (let c = 0; c < w; c++) {
      const color = sprite[r][c];
      if (color && color !== "") {
        ctx.fillStyle = color;
        ctx.fillRect(c * zoom, r * zoom, zoom, zoom);
      }
    }
  }

  if (key) spriteCanvasCache.set(key, canvas);
  return canvas;
}

/** Clear sprite cache (on zoom change) */
export function clearSpriteCache(): void {
  spriteCanvasCache.clear();
}
