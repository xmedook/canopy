/** Generate a deterministic palette for an agent based on hash */
export function agentPalette(hash: number): {
  body: string;
  skin: string;
  hair: string;
  accent: string;
} {
  const hue = hash % 360;
  const skin = SKIN_TONES[hash % SKIN_TONES.length];
  const hair = HAIR_COLORS[(hash >> 4) % HAIR_COLORS.length];
  return {
    body: `hsl(${hue}, 55%, 42%)`,
    skin,
    hair,
    accent: `hsl(${(hue + 30) % 360}, 60%, 55%)`,
  };
}

