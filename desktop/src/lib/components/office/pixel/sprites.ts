/** Mirror a sprite horizontally */
function mirrorSprite(sprite: PaletteKey[][]): PaletteKey[][] {
  return sprite.map((row) => [...row].reverse());
}

/** Get all frames for a character direction + state */
export function getCharFrames(dir: Direction, state: string): PaletteKey[][][] {
  switch (dir) {
    case "down":
      if (state === "walk")
        return [CHAR_DOWN, CHAR_DOWN_WALK1, CHAR_DOWN, CHAR_DOWN_WALK2];
      if (state === "type") return [CHAR_DOWN_TYPE, CHAR_DOWN_TYPE2];
      if (state === "sleep") return [CHAR_DOWN_SLEEP];
      return [CHAR_DOWN];
    case "up":
      if (state === "walk") return [CHAR_UP]; // simplified — just 1 frame
      return [CHAR_UP];
    case "right":
      if (state === "walk") return [CHAR_RIGHT]; // simplified
      return [CHAR_RIGHT];
    case "left": {
      const frames = getCharFrames("right", state);
      return frames.map((f) => mirrorSprite(f));
    }
  }
}

/** Resolve palette key to actual color */
export function resolveColor(
  key: PaletteKey,
  palette: { body: string; skin: string; hair: string; accent: string },
): string | null {
  switch (key) {
    case "_":
      return null;
    case "s":
      return palette.skin;
    case "h":
      return palette.hair;
    case "b":
      return palette.body;
    case "a":
      return palette.accent;
    case "e":
      return "#1a1a2e";
    case "m":
      return "#c4727a";
    case "S":
      return "#2a2a3c";
    case "o":
      return "#1e1e30";
    default:
      return null;
  }
}

