#!/usr/bin/env python3
"""
Dynamic Skill Generator - Creates new skills from detected patterns
Based on: ReCreate Framework (arXiv:2601.11100)

Automatically generates reusable skills when patterns repeat 5+ times.
"""

import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path
from collections import Counter
import hashlib

LEARNING_DIR = Path.home() / ".claude" / "learning"
PATTERNS_DIR = LEARNING_DIR / "patterns"
SKILLS_DIR = Path.home() / ".claude" / "skills" / "generated"
SKILLS_DIR.mkdir(parents=True, exist_ok=True)

PATTERN_THRESHOLD = 5  # Number of occurrences before skill generation


def get_timestamp():
    """Get ISO timestamp."""
    return datetime.now(timezone.utc).isoformat()


def load_patterns(domain: str) -> list:
    """Load patterns for a specific domain."""
    patterns = []
    pattern_file = PATTERNS_DIR / domain / "patterns.jsonl"

    if not pattern_file.exists():
        return patterns

    with open(pattern_file) as f:
        for line in f:
            if line.strip():
                try:
                    patterns.append(json.loads(line))
                except json.JSONDecodeError:
                    continue

    return patterns


def analyze_pattern_frequency(patterns: list) -> dict:
    """Analyze which patterns appear frequently."""
    # Group by pattern type
    type_counter = Counter(p.get("type") for p in patterns)

    # Group by hash (exact matches)
    hash_counter = Counter()
    for p in patterns:
        hash_key = p.get("snippet_hash", p.get("full_command_hash", "unknown"))
        hash_counter[hash_key] += 1

    # Find patterns above threshold
    frequent_patterns = {
        "by_type": {k: v for k, v in type_counter.items() if v >= PATTERN_THRESHOLD},
        "by_hash": {k: v for k, v in hash_counter.items() if v >= PATTERN_THRESHOLD}
    }

    return frequent_patterns


def generate_skill_from_pattern(pattern_type: str, domain: str, patterns: list) -> dict:
    """Generate a skill definition from repeated patterns."""
    # Filter patterns of this type
    matching = [p for p in patterns if p.get("type") == pattern_type]

    if not matching:
        return None

    # Extract common characteristics
    skill_id = f"{domain}-{pattern_type}-auto"
    skill_id = skill_id.replace("_", "-").lower()

    # Collect unique file patterns
    file_patterns = list(set(p.get("file_pattern", "") for p in matching if p.get("file_pattern")))

    # Determine language/domain
    languages = list(set(p.get("language", domain) for p in matching if p.get("language")))

    skill_definition = {
        "id": skill_id,
        "name": f"Auto-generated {pattern_type.replace('_', ' ').title()} Skill",
        "description": f"Automatically generated skill for {domain} {pattern_type} patterns. Created from {len(matching)} observed instances.",
        "domain": domain,
        "pattern_type": pattern_type,
        "generated": True,
        "created_at": get_timestamp(),
        "instance_count": len(matching),
        "metadata": {
            "file_patterns": file_patterns[:5],
            "languages": languages,
            "confidence": min(1.0, len(matching) / 20)  # Higher count = higher confidence
        }
    }

    return skill_definition


def write_skill_file(skill: dict):
    """Write skill definition to SKILL.md file."""
    skill_dir = SKILLS_DIR / skill["id"]
    skill_dir.mkdir(exist_ok=True)

    skill_md = f"""---
name: {skill['id']}
description: {skill['description']}
metadata:
  generated: true
  domain: {skill['domain']}
  pattern_type: {skill['pattern_type']}
  instance_count: {skill['instance_count']}
  confidence: {skill['metadata']['confidence']:.2f}
  created_at: {skill['created_at']}
---

# {skill['name']}

## Overview

This skill was automatically generated by the learning engine based on {skill['instance_count']} observed pattern instances in the `{skill['domain']}` domain.

## When to Use

Use this skill when working with:
- Domain: {skill['domain']}
- Pattern type: {skill['pattern_type'].replace('_', ' ')}
- File patterns: {', '.join(skill['metadata']['file_patterns']) or 'Any'}

## Confidence

This skill has a confidence score of {skill['metadata']['confidence']:.0%} based on the number of successful pattern instances observed.

## Languages

{', '.join(skill['metadata']['languages']) or 'General'}

## Notes

This is an auto-generated skill. It will be improved as more patterns are observed.
Review and customize as needed.
"""

    skill_file = skill_dir / "SKILL.md"
    with open(skill_file, "w") as f:
        f.write(skill_md)

    # Also save JSON definition
    json_file = skill_dir / "definition.json"
    with open(json_file, "w") as f:
        json.dump(skill, f, indent=2)

    return skill_file


def update_skill_index(skills: list):
    """Update the generated skills index."""
    index_file = SKILLS_DIR / "index.json"

    existing = []
    if index_file.exists():
        with open(index_file) as f:
            existing = json.load(f)

    # Merge, avoiding duplicates
    existing_ids = {s["id"] for s in existing}
    for skill in skills:
        if skill["id"] not in existing_ids:
            existing.append({
                "id": skill["id"],
                "name": skill["name"],
                "domain": skill["domain"],
                "pattern_type": skill["pattern_type"],
                "confidence": skill["metadata"]["confidence"],
                "created_at": skill["created_at"]
            })

    # Update timestamps
    for s in existing:
        if s["id"] in [skill["id"] for skill in skills]:
            s["last_updated"] = get_timestamp()

    with open(index_file, "w") as f:
        json.dump(existing, f, indent=2)


def main():
    """Main skill generation process."""
    generated_skills = []

    # Get all pattern domains
    if not PATTERNS_DIR.exists():
        print(json.dumps({"status": "no_patterns", "skills_generated": 0}))
        return

    domains = [d.name for d in PATTERNS_DIR.iterdir() if d.is_dir()]

    for domain in domains:
        patterns = load_patterns(domain)

        if not patterns:
            continue

        # Analyze frequency
        frequency = analyze_pattern_frequency(patterns)

        # Generate skills for frequent patterns
        for pattern_type, count in frequency["by_type"].items():
            skill = generate_skill_from_pattern(pattern_type, domain, patterns)

            if skill:
                skill_file = write_skill_file(skill)
                generated_skills.append(skill)
                print(f"Generated skill: {skill['id']} ({count} instances)", file=sys.stderr)

    # Update index
    if generated_skills:
        update_skill_index(generated_skills)

    result = {
        "status": "success",
        "skills_generated": len(generated_skills),
        "skills": [s["id"] for s in generated_skills],
        "timestamp": get_timestamp()
    }

    print(json.dumps(result))


if __name__ == "__main__":
    main()
